	/**
	 * 保存话题
	 * @return
	 */
	function save() {
        $data = $_POST;
        Utils::JsonFromInput( $data );
		$inst = new \Xpmsns\Pages\Model\Topic;
		$rs = $inst->saveByTopicId( $data );
		echo json_encode($rs);
	}

	/**
	 * 删除话题
	 * @return [type] [description]
	 */
	function remove(){
		$topic_id = $_POST['topic_id'];
		$inst = new \Xpmsns\Pages\Model\Topic;
		$topic_ids =$inst->remove( $topic_id, "topic_id" );
		echo json_encode(['message'=>"删除成功", 'extra'=>['$topic_ids'=>$topic_ids]]);
	}

